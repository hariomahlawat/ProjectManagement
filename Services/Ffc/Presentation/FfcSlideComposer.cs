    public FfcSlideComposer(
        IFfcPresentationMapRenderer mapRenderer,
        IWebHostEnvironment environment)
    {
        _mapRenderer = mapRenderer ?? throw new ArgumentNullException(nameof(mapRenderer));
        ArgumentNullException.ThrowIfNull(environment);

        _templatePath = Path.Combine(
            environment.ContentRootPath,
            "Resources",
            "FfcPresentation",
            "FfcPortfolioTemplate.pptx");

        var logoCandidate = Path.Combine(environment.WebRootPath, "img", "logos", "sdd.png");
        _logoPath = File.Exists(logoCandidate) ? logoCandidate : null;
    }

    public (byte[] Content, int SlideCount) Compose(FfcPresentationData data)
    {
        ArgumentNullException.ThrowIfNull(data);
        if (!File.Exists(_templatePath))
        {
            throw new FileNotFoundException("FFC presentation template was not found.", _templatePath);
        }

        var plans = BuildPlans(data);
        var templateBytes = File.ReadAllBytes(_templatePath);
        using var stream = new MemoryStream(templateBytes.Length + 2_000_000);
        stream.Write(templateBytes, 0, templateBytes.Length);
        stream.Position = 0;

        using (var document = PresentationDocument.Open(stream, true))
        {
            var presentationPart = document.PresentationPart
                ?? throw new InvalidOperationException("The PowerPoint template has no presentation part.");
            var layoutPart = FindBlankLayout(presentationPart)
                ?? throw new InvalidOperationException("The PowerPoint template has no slide layout.");

            RemoveTemplateSlides(presentationPart);
            var slideIdList = presentationPart.Presentation.SlideIdList;
            if (slideIdList is null)
            {
                slideIdList = new SlideIdList();
                presentationPart.Presentation.Append(slideIdList);
            }
            uint nextSlideId = 256;
            var logo = _logoPath is null ? null : File.ReadAllBytes(_logoPath);
            var mapImage = _mapRenderer.Render(data.Countries);

            for (var index = 0; index < plans.Count; index++)
            {
                var slidePart = presentationPart.AddNewPart<SlidePart>();
                slidePart.AddPart(layoutPart);
                var canvas = new SlideCanvas(slidePart);
                plans[index].Render(canvas, mapImage, logo);
                AddStandardFooter(canvas, data, index + 1, plans.Count, plans[index].IsCover);
                canvas.Commit();

                var relationshipId = presentationPart.GetIdOfPart(slidePart);
                slideIdList.Append(new SlideId
                {
                    Id = nextSlideId++,
                    RelationshipId = relationshipId
                });
            }

            presentationPart.Presentation.Save();
            document.PackageProperties.Title = data.Title;
            document.PackageProperties.Subject = "FFC portfolio briefing generated by PRISM ERP";
            document.PackageProperties.Creator = "Simulator Development Division";
            document.PackageProperties.LastModifiedBy = "PRISM ERP";
            document.PackageProperties.Modified = DateTime.UtcNow;
        }

        return (stream.ToArray(), plans.Count);
    }

    private List<SlidePlan> BuildPlans(FfcPresentationData data)
    {
        var plans = new List<SlidePlan>
        {
            new(true, (canvas, _, logo) => RenderCover(canvas, data, logo)),
            new(false, (canvas, _, _) => RenderAtAGlance(canvas, data)),
            new(false, (canvas, map, _) => RenderGlobalFootprint(canvas, data, map))
        };

        AddCountryUnitPositionSlides(plans, data);
        AddMilestoneSlides(plans, data);
        plans.Add(new SlidePlan(false, (canvas, _, _) => RenderOperationalFocus(canvas, data)));

        if (data.PresentationType == FfcPresentationType.FullPortfolio)
        {
            foreach (var country in data.Countries)
            {
                var capturedCountry = country;
                plans.Add(new SlidePlan(false, (canvas, _, _) => RenderCountrySummary(canvas, data, capturedCountry)));

                if (data.IncludeProjects)
                {
                    AddProjectSlides(plans, data, capturedCountry);
                }

                if (data.IncludeAttachmentRegister && capturedCountry.Records.Any(record => record.Attachments.Count > 0))
                {
                    AddAttachmentSlides(plans, data, capturedCountry);
                }
            }

            AddAppendixSlides(plans, data);
        }

        return plans;
    }

