    private static void AddSegmentedBar(
        SlideCanvas canvas,
        double x,
        double y,
        double width,
        double height,
        int installed,
        int delivered,
        int planned)
    {
        var total = installed + delivered + planned;
        canvas.AddRoundedRect(x, y, width, height, "E3E8EF", "E3E8EF", .05);
        if (total <= 0)
        {
            return;
        }
        var installedWidth = width * installed / total;
        var deliveredWidth = width * delivered / total;
        var plannedWidth = Math.Max(0, width - installedWidth - deliveredWidth);
        if (installedWidth > 0) canvas.AddRect(x, y, installedWidth, height, Green);
        if (deliveredWidth > 0) canvas.AddRect(x + installedWidth, y, deliveredWidth, height, DeliveredBlue);
        if (plannedWidth > 0) canvas.AddRect(x + installedWidth + deliveredWidth, y, plannedWidth, height, PlannedGray);
    }

