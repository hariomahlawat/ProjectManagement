    private static void AddProjectSlides(
        List<SlidePlan> plans,
        FfcPresentationData data,
        FfcPresentationCountry country)
    {
        var projects = country.Records
            .SelectMany(record => record.Projects.Select(project => (Record: record, Project: project)))
            .ToArray();
        if (projects.Length == 0)
        {
            return;
        }

        const int rowsPerSlide = 6;
        var pages = (int)Math.Ceiling(projects.Length / (double)rowsPerSlide);
        for (var page = 0; page < pages; page++)
        {
            var pageRows = projects.Skip(page * rowsPerSlide).Take(rowsPerSlide).ToArray();
            var capturedPage = page;
            plans.Add(new SlidePlan(false, (canvas, _, _) =>
                RenderProjectDetails(canvas, data, country, pageRows, capturedPage + 1, pages)));
        }
    }

    private static void RenderProjectDetails(
        SlideCanvas canvas,
        FfcPresentationData data,
        FfcPresentationCountry country,
        IReadOnlyList<(FfcPresentationRecord Record, FfcPresentationProject Project)> rows,
        int page,
        int pages)
    {
        AddSlideTitle(
            canvas,
            $"{country.CountryName} · project status",
            pages > 1 ? $"Project-level quantity and progress · {page} of {pages}" : "Project-level quantity and progress");

        var widths = data.IncludeProgress
            ? new[] { .8, 3.55, .65, 1.25, 5.68 }
            : new[] { .8, 5.2, .75, 1.45, 3.73 };
        var headers = new[] { "YEAR", "PROJECT", "QTY", "STATUS", data.IncludeProgress ? "CURRENT PROGRESS" : "CURRENT STAGE" };
        var tableRows = new List<IReadOnlyList<NativeTableCell>> { HeaderRow(headers) };
        var heights = new List<double> { .42 };

        foreach (var row in rows)
        {
            var narrative = data.IncludeProgress
                ? row.Project.CurrentProgress ?? row.Project.StageSummary
                : row.Project.StageSummary;

            tableRows.Add(new[]
            {
                Cell(row.Record.Year.ToString(CultureInfo.InvariantCulture), 10),
                Cell(Truncate(row.Project.Name, 66) ?? "FFC project", 11, Text, true),
                Cell(row.Project.Quantity.ToString(CultureInfo.InvariantCulture), 11, Text, true, "r"),
                Cell(PositionLabel(row.Project.Position), 10, PositionColor(row.Project.Position), true),
                Cell(Truncate(narrative, 150) ?? "No current progress recorded.", 10, Muted)
            });
            heights.Add(.82);
        }

