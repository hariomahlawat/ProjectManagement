    private static void RenderAtAGlance(SlideCanvas canvas, FfcPresentationData data)
    {
        AddSlideTitle(canvas, "Portfolio at a glance", "Current FFC portfolio scale and quantity status");
        var summary = data.Summary;
        var cards = new[]
        {
            ("COUNTRIES", summary.CountryCount),
            ("COUNTRY-YEAR RECORDS", summary.RecordCount),
            ("PROJECTS", summary.ProjectCount),
            ("TOTAL QUANTITY", summary.TotalUnits)
        };

        for (var index = 0; index < cards.Length; index++)
        {
            var x = .65 + (index * 3.08);
            canvas.AddRoundedRect(x, 1.35, 2.72, 1.45, CardBackground, Border, .09);
            canvas.AddText(x + .22, 1.68, 2.25, .52, cards[index].Item2.ToString(CultureInfo.InvariantCulture), 28, Text, true, "l");
            canvas.AddText(x + .22, 2.27, 2.25, .3, cards[index].Item1, 11, Muted, true, "l");
        }

        canvas.AddText(.7, 3.28, 5.2, .36, "QUANTITY STATUS", 12, Muted, true, "l");
        AddSegmentedBar(canvas, .7, 3.82, 11.95, .34, summary.InstalledUnits, summary.DeliveredNotInstalledUnits, summary.PlannedUnits);

        AddMetricLegend(canvas, .75, 4.38, "Installed", summary.InstalledUnits, Green);
        AddMetricLegend(canvas, 4.45, 4.38, "Delivered, awaiting installation", summary.DeliveredNotInstalledUnits, DeliveredBlue);
        AddMetricLegend(canvas, 9.2, 4.38, "Planned", summary.PlannedUnits, PlannedGray);

        var deliveredPercent = summary.TotalUnits == 0 ? 0 : summary.DeliveredUnits * 100d / summary.TotalUnits;
        var installedPercent = summary.TotalUnits == 0 ? 0 : summary.InstalledUnits * 100d / summary.TotalUnits;
        canvas.AddRoundedRect(.7, 5.2, 5.65, 1.12, "EFF4FF", "D8E7FF", .08);
        canvas.AddText(.95, 5.45, 1.2, .48, $"{deliveredPercent:0}%", 27, Blue, true, "l");
        canvas.AddText(2.15, 5.48, 3.85, .4, "of total quantity delivered", 15, Text, true, "l");
        canvas.AddRoundedRect(6.58, 5.2, 5.65, 1.12, "ECF8F0", "D1EEDD", .08);
        canvas.AddText(6.83, 5.45, 1.2, .48, $"{installedPercent:0}%", 27, Green, true, "l");
        canvas.AddText(8.03, 5.48, 3.85, .4, "of total quantity installed", 15, Text, true, "l");
    }

    private static void RenderGlobalFootprint(SlideCanvas canvas, FfcPresentationData data, byte[] mapImage)
    {
        AddSlideTitle(canvas, "Global footprint", "Country distribution by total quantity");
        canvas.AddImage(mapImage, .55, 1.28, 8.55, 5.62, "FFC global footprint map");
        canvas.AddRoundedRect(9.3, 1.28, 3.42, 5.62, CardBackground, Border, .08);
        canvas.AddText(9.58, 1.58, 2.8, .32, "COUNTRY / QTY", 12, Muted, true, "l");

        var rows = data.Countries.Take(9).ToArray();
        var y = 2.05;
        for (var index = 0; index < rows.Length; index++)
        {
            var country = rows[index];
            canvas.AddText(9.58, y, 2.12, .28, country.CountryName, 13, Text, true, "l");
            canvas.AddText(11.78, y, .52, .28, country.TotalUnits.ToString(CultureInfo.InvariantCulture), 13, Text, true, "r");
            canvas.AddText(12.18, y, .32, .28, "Qty", 8, Muted, false, "l");
            if (index < rows.Length - 1)
            {
                canvas.AddLine(9.58, y + .39, 12.46, y + .39, Border, .6);
            }
            y += .48;
        }

        if (data.Countries.Count > rows.Length)
        {
            canvas.AddText(9.58, 6.48, 2.8, .25, $"+ {data.Countries.Count - rows.Length} more countr{(data.Countries.Count - rows.Length == 1 ? "y" : "ies")}", 10, Muted, false, "l");
        }
    }

    private static void AddCountryUnitPositionSlides(
        List<SlidePlan> plans,
        FfcPresentationData data)
    {
        const int rowsPerSlide = 12;
        var countries = data.Countries.ToArray();
        if (countries.Length == 0)
        {
            plans.Add(new SlidePlan(false, (canvas, _, _) =>
            {
                AddSlideTitle(canvas, "Country-wise quantity status", "Installed, delivered and planned quantities by country");
                AddEmptyMessage(canvas, "No country data is available for this export scope.");
            }));
            return;
        }

        var pageCount = (int)Math.Ceiling(countries.Length / (double)rowsPerSlide);
        for (var page = 0; page < pageCount; page++)
        {
            var rows = countries.Skip(page * rowsPerSlide).Take(rowsPerSlide).ToArray();
            var capturedPage = page;
            plans.Add(new SlidePlan(false, (canvas, _, _) =>
                RenderCountryUnitPosition(canvas, data, rows, capturedPage + 1, pageCount)));
        }
    }

    private static void RenderCountryUnitPosition(
        SlideCanvas canvas,
        FfcPresentationData data,
        IReadOnlyList<FfcPresentationCountry> countries,
        int page,
        int totalPages)
    {
        AddSlideTitle(
            canvas,
            "Country-wise quantity status",
            totalPages > 1
                ? $"Installed, delivered and planned quantities by country · {page} of {totalPages}"
                : "Installed, delivered and planned quantities by country");
        if (countries.Count == 0)
        {
            AddEmptyMessage(canvas, "No country data is available for this export scope.");
            return;
        }

        var maximum = Math.Max(1, data.Countries.Max(country => country.TotalUnits));
        var y = 1.35;
        foreach (var country in countries)
        {
            canvas.AddText(.65, y, 2.4, .27, country.CountryName, 12, Text, true, "l");
            canvas.AddText(2.65, y, .36, .27, country.TotalUnits.ToString(CultureInfo.InvariantCulture), 11, Muted, true, "r");
            var barX = 3.2;
            var barWidth = 8.75;
            var activeWidth = country.TotalUnits / (double)maximum * barWidth;
            var installedWidth = country.TotalUnits == 0 ? 0 : activeWidth * country.InstalledUnits / country.TotalUnits;
            var deliveredWidth = country.TotalUnits == 0 ? 0 : activeWidth * country.DeliveredNotInstalledUnits / country.TotalUnits;
            var plannedWidth = Math.Max(0, activeWidth - installedWidth - deliveredWidth);
            canvas.AddRoundedRect(barX, y + .04, barWidth, .22, "E5EAF0", "E5EAF0", .03);
            if (installedWidth > 0) canvas.AddRect(barX, y + .04, installedWidth, .22, Green);
            if (deliveredWidth > 0) canvas.AddRect(barX + installedWidth, y + .04, deliveredWidth, .22, DeliveredBlue);
            if (plannedWidth > 0) canvas.AddRect(barX + installedWidth + deliveredWidth, y + .04, plannedWidth, .22, PlannedGray);
            canvas.AddText(12.08, y, .56, .27, $"{country.InstalledUnits}/{country.DeliveredNotInstalledUnits}/{country.PlannedUnits}", 9, Muted, false, "r");
            y += .43;
        }

        AddMetricLegend(canvas, .7, 6.65, "Installed", data.Summary.InstalledUnits, Green);
        AddMetricLegend(canvas, 4.4, 6.65, "Delivered", data.Summary.DeliveredNotInstalledUnits, DeliveredBlue);
        AddMetricLegend(canvas, 8.1, 6.65, "Planned", data.Summary.PlannedUnits, PlannedGray);
    }

    private static void AddMilestoneSlides(List<SlidePlan> plans, FfcPresentationData data)
    {
        var records = data.Countries
            .SelectMany(country => country.Records.Select(record => (Country: country, Record: record)))
            .OrderByDescending(item => item.Record.Year)
            .ThenBy(item => item.Country.CountryName, StringComparer.OrdinalIgnoreCase)
            .ToArray();

        const int rowsPerSlide = 12;
        if (records.Length == 0)
        {
            plans.Add(new SlidePlan(false, (canvas, _, _) =>
            {
                AddSlideTitle(canvas, "IPA and GSL status", "Milestone status across country-year records");
                AddEmptyMessage(canvas, "No country-year records are available for this export scope.");
            }));
            return;
        }

        var pageCount = (int)Math.Ceiling(records.Length / (double)rowsPerSlide);
        for (var page = 0; page < pageCount; page++)
        {
            var pageRows = records.Skip(page * rowsPerSlide).Take(rowsPerSlide).ToArray();
            var capturedPage = page;
            plans.Add(new SlidePlan(false, (canvas, _, _) =>
                RenderMilestoneTable(canvas, data, pageRows, capturedPage + 1, pageCount)));
        }
    }

    private static void RenderMilestoneTable(
        SlideCanvas canvas,
        FfcPresentationData data,
        IReadOnlyList<(FfcPresentationCountry Country, FfcPresentationRecord Record)> rows,
        int page,
        int totalPages)
    {
        AddSlideTitle(
            canvas,
            "IPA and GSL status",
            totalPages > 1
                ? $"Milestone status across country-year records · {page} of {totalPages}"
                : "Milestone status across country-year records");

        var widths = new[] { 2.8, .75, 2.35, 2.35, .8, 3.25 };
        var tableRows = new List<IReadOnlyList<NativeTableCell>>
        {
            HeaderRow("COUNTRY", "YEAR", "IPA", "GSL", "QTY", "OVERALL STATUS")
        };
        var heights = new List<double> { .42 };

        for (var rowIndex = 0; rowIndex < rows.Count; rowIndex++)
        {
            var item = rows[rowIndex];
            var fill = rowIndex % 2 == 0 ? "FFFFFF" : "F7F9FC";
            tableRows.Add(new[]
            {
                Cell(item.Country.CountryName, 10, Text, true, "l", fill),
                Cell(item.Record.Year.ToString(CultureInfo.InvariantCulture), 10, Text, false, "ctr", fill),
                Cell(MilestoneShort(item.Record.IpaCompleted, item.Record.IpaDate), 9, MilestoneColor(item.Record.IpaCompleted, item.Record.IpaDate), true, "l", fill),
                Cell(MilestoneShort(item.Record.GslCompleted, item.Record.GslDate), 9, MilestoneColor(item.Record.GslCompleted, item.Record.GslDate), true, "l", fill),
                Cell(item.Record.TotalUnits.ToString(CultureInfo.InvariantCulture), 10, Text, true, "r", fill),
                Cell(Truncate(item.Record.OverallPosition, 58) ?? "—", 9, Muted, false, "l", fill)
            });
            heights.Add(.43);
        }

        canvas.AddNativeTable(.65, 1.25, widths, heights, tableRows, "IPA and GSL status table");

        if (data.IncludeMilestoneRemarks)
        {
            var remarks = rows
                .SelectMany(item => new[]
                {
                    string.IsNullOrWhiteSpace(item.Record.IpaRemarks) ? null : $"{item.Country.CountryName} {item.Record.Year} IPA: {item.Record.IpaRemarks}",
                    string.IsNullOrWhiteSpace(item.Record.GslRemarks) ? null : $"{item.Country.CountryName} {item.Record.Year} GSL: {item.Record.GslRemarks}"
                })
                .Where(value => value is not null)
                .Take(3)
                .ToArray();
            if (remarks.Length > 0)
            {
                canvas.AddText(.72, 6.72, 11.9, .34, string.Join("  ·  ", remarks.Select(remark => Truncate(remark, 115))), 8, Muted, false, "l");
            }
        }
    }

    private static void RenderOperationalFocus(SlideCanvas canvas, FfcPresentationData data)
    {
        AddSlideTitle(canvas, "Operational focus", "Portfolio areas requiring monitoring or follow-up");

        var planned = data.Countries
            .Where(country => country.PlannedUnits > 0)
            .OrderByDescending(country => country.PlannedUnits)
            .Take(5)
            .ToArray();
        var pendingMilestones = data.Countries
            .SelectMany(country => country.Records.Select(record => (Country: country, Record: record)))
            .Where(item => !item.Record.IpaCompleted || !item.Record.GslCompleted)
            .OrderByDescending(item => item.Record.TotalUnits)
            .Take(5)
            .ToArray();
        var partial = data.Countries
            .Where(country => country.DeliveredNotInstalledUnits > 0)
            .OrderByDescending(country => country.DeliveredNotInstalledUnits)
            .Take(5)
            .ToArray();

        AddFocusColumn(canvas, .65, 1.35, 3.9, 4.95, "PLANNED PIPELINE", Blue,
            planned.Select(country => $"{country.CountryName} · {country.PlannedUnits} planned").ToArray(),
            "No planned quantity in scope.");
        AddFocusColumn(canvas, 4.72, 1.35, 3.9, 4.95, "MILESTONE ATTENTION", Amber,
            pendingMilestones.Select(item =>
            {
                var pending = new List<string>();
                if (!item.Record.IpaCompleted) pending.Add("IPA");
                if (!item.Record.GslCompleted) pending.Add("GSL");
                return $"{item.Country.CountryName} {item.Record.Year} · {string.Join(" + ", pending)} pending";
            }).ToArray(),
            "No pending IPA/GSL milestones in scope.");
        AddFocusColumn(canvas, 8.79, 1.35, 3.9, 4.95, "AWAITING INSTALLATION", DeliveredBlue,
            partial.Select(country => $"{country.CountryName} · {country.DeliveredNotInstalledUnits} awaiting installation").ToArray(),
            "No delivered quantity awaiting installation.");
    }

