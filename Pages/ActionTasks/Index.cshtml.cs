    public string ResolveSprintName(int? sprintId)
    {
        if (!sprintId.HasValue)
        {
            return "Backlog";
        }

        var sprint = Sprints.FirstOrDefault(s => s.Id == sprintId.Value);
        return sprint is null ? $"Sprint #{sprintId.Value}" : DisplaySprintName(sprint);
    }

    public string DisplaySprintName(ActionSprint? sprint)
    {
        // SECTION: Normalize known auto-generated sprint labels while preserving user-authored names.
        if (sprint is null)
        {
            return "Sprint";
        }

        var name = sprint.Name?.Trim();
        if (string.IsNullOrWhiteSpace(name))
        {
            return $"Sprint #{sprint.Id}";
        }

        var autoGeneratedMatch = AutoGeneratedSprintNamePattern.Match(name);
        return autoGeneratedMatch.Success
            ? $"Sprint {autoGeneratedMatch.Groups["range"].Value}"
            : name;
    }

