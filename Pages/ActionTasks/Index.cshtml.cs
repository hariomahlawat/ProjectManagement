    // SECTION: Create action task
    public async Task<IActionResult> OnPostCreateAsync()
    {
        await ResolveIdentityAsync();

        if (!_permission.CanCreate(CurrentRole))
        {
            return Forbid();
        }

        if (!ModelState.IsValid)
        {
            ShowCreateModal = true;
            await LoadDataAsync();
            return Page();
        }

        // SECTION: Business validation for due-date discipline
        if (Input.DueDate.Date < _clock.UtcToday)
        {
            ModelState.AddModelError(nameof(Input.DueDate), "Due date cannot be in the past.");
            ShowCreateModal = true;
            await LoadDataAsync();
            return Page();
        }

        var assignedUser = await _users.FindByIdAsync(Input.AssignedToUserId);
        if (assignedUser is null)
        {
            ModelState.AddModelError(nameof(Input.AssignedToUserId), "Selected user was not found.");
            ShowCreateModal = true;
            await LoadDataAsync();
            return Page();
        }

        if (assignedUser.IsDisabled || assignedUser.PendingDeletion)
        {
            ModelState.AddModelError(nameof(Input.AssignedToUserId), "Selected user is inactive and cannot be assigned a task.");
            ShowCreateModal = true;
            await LoadDataAsync();
            return Page();
        }

        if (assignedUser.LockoutEnd.HasValue && assignedUser.LockoutEnd > new DateTimeOffset(_clock.UtcNow, TimeSpan.Zero))
        {
            ModelState.AddModelError(nameof(Input.AssignedToUserId), "Selected user is locked and cannot be assigned a task.");
            ShowCreateModal = true;
            await LoadDataAsync();
            return Page();
        }

        var assignedRoles = await _users.GetRolesAsync(assignedUser);
        var assignedRole = ActionTaskRoleResolver.ResolveAssignableRoleFromRoles(assignedRoles);
        if (assignedRole is null)
        {
            ModelState.AddModelError(string.Empty, "Selected user does not have an assignable Task Tracker role.");
            ShowCreateModal = true;
            await LoadDataAsync();
            return Page();
        }

        if (!_permission.CanAssign(CurrentRole, assignedRole))
        {
            ModelState.AddModelError(string.Empty, $"Current role is not permitted to assign tasks to {assignedRole}.");
            ShowCreateModal = true;
            await LoadDataAsync();
            return Page();
        }

        await _service.CreateTaskAsync(new ActionTaskItem
        {
            Title = Input.Title.Trim(),
            Description = Input.Description.Trim(),
            CreatedByUserId = CurrentUserId,
            AssignedToUserId = Input.AssignedToUserId,
            CreatedByRole = CurrentRole,
            AssignedToRole = assignedRole,
            DueDate = Input.DueDate,
            Priority = Input.Priority
        });

        TempData["ToastMessage"] = "Task created.";
        return RedirectToPage("/ActionTasks/Index", BuildTaskWorkspaceRouteValues(TaskId, SelectedSprintId));
    }



    // SECTION: Create sprint from Sprint Board workspace through sprint service boundary.
    public async Task<IActionResult> OnPostCreateSprintAsync()
    {
        await ResolveIdentityAsync();
        ViewMode = "Planning";
        ModelState.Clear();
        TryValidateModel(SprintInput, nameof(SprintInput));
        ValidateSprintDateRange(SprintInput.StartDate, SprintInput.EndDate, nameof(SprintInput));

        if (!ModelState.IsValid)
        {
            ShowCreateSprintPanel = true;
            await LoadDataAsync();
            return Page();
        }

        try
        {
            var sprint = await _sprintService.CreateSprintAsync(new ActionSprint
            {
                Name = SprintInput.Name.Trim(),
                Goal = SprintInput.Goal?.Trim(),
                StartDate = SprintInput.StartDate,
                EndDate = SprintInput.EndDate
            }, CurrentUserId, CurrentRole);
            TempData["ToastMessage"] = "Sprint created.";
            return RedirectToPage(new { ViewMode = "Planning", SelectedSprintId = sprint.Id });
        }
        catch (InvalidOperationException ex)
        {
            ModelState.AddModelError(string.Empty, ex.Message);
            ShowCreateSprintPanel = true;
            await LoadDataAsync();
            return Page();
        }
    }

    // SECTION: Update selected non-closed sprint through sprint service boundary.
    public async Task<IActionResult> OnPostUpdateSprintAsync()
    {
        await ResolveIdentityAsync();
        ViewMode = "Planning";
        SelectedSprintId = SprintEditInput.SprintId;
        ModelState.Clear();
        TryValidateModel(SprintEditInput, nameof(SprintEditInput));
        ValidateSprintDateRange(SprintEditInput.StartDate, SprintEditInput.EndDate, nameof(SprintEditInput));

        if (!ModelState.IsValid)
        {
            ShowEditSprintPanel = true;
            await LoadDataAsync();
            return Page();
        }

        try
        {
            await _sprintService.UpdateSprintAsync(
                SprintEditInput.SprintId,
                DecodeSprintRowVersion(SprintEditInput.RowVersion),
                SprintEditInput.Name.Trim(),
                SprintEditInput.Goal?.Trim(),
                SprintEditInput.StartDate,
                SprintEditInput.EndDate,
                CurrentUserId,
                CurrentRole);
            TempData["ToastMessage"] = "Sprint updated.";
            return RedirectToPage(new { ViewMode = "Planning", SelectedSprintId = SprintEditInput.SprintId });
        }
        catch (ActionTaskConcurrencyException ex)
        {
            ModelState.AddModelError(string.Empty, ex.Message);
        }
        catch (InvalidOperationException ex)
        {
            ModelState.AddModelError(string.Empty, ex.Message);
        }

        ShowEditSprintPanel = true;
        await LoadDataAsync();
        return Page();
    }

